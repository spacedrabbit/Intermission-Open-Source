import UIKit

extension CGRect {
    
    /// Convenience for self.size.height
    public var h: CGFloat { return self.size.height }
    
    /// Convenience for self.size.width
    public var w: CGFloat { return self.size.width }
    
    /// Convenience for self.origin.x
    public var x: CGFloat { return self.origin.x }
    
    /// Convenience for self.origin.y
    public var y: CGFloat { return self.origin.y }
    
}
